let handler = async (m, { command, args, usedPrefix }) => {
    let user = global.db.data.users[m.sender]
    let listCrate = Object.fromEntries(Object.entries(rewards).filter(([v]) => v && v in user))
    let info = `
استخدك الامر *${usedPrefix}${command} [عنصر] [عدد]*
مثال: *${usedPrefix}${command} صندوق اسطوري 10*
📍قائمة العناصر: 
${Object.keys(listCrate).map((v) => `
${rpg.emoticon(v)}${v}
`.trim()).join('\n')}
`.trim()
    let type = (args[0] || '').toLowerCase()
    let count = Math.floor(isNumber(args[1]) ? Math.min(Math.max(parseInt(args[1]), 1), Number.MAX_SAFE_INTEGER) : 1) * 1
    if (!(type in listCrate)) return m.reply(info)
    if (user[type] < count) return m.reply(`
 *${rpg.emoticon(type)}${type} عنصرك غير كافي!, لديك فقط ${user[type]} *${rpg.emoticon(type)}${type} عنصر*
اكتب *${usedPrefix}شراء ${type} ${count - user[type]}* للشراء
`.trim())
    // TODO: add pet crate
    // if (type !== 'pet')
    let crateReward = {}
    for (let i = 0; i < count; i++)
        for (let [reward, value] of Object.entries(listCrate[type]))
            if (reward in user) {
                const total = value.getRandom()
                if (total) {
                    user[reward] += total * 1
                    crateReward[reward] = (crateReward[reward] || 0) + (total * 1)
                }
            }
    user[type] -= count * 1
    m.reply(`
لقد تم فتح *${count}* ${global.rpg.emoticon(type)}${type}  حصلت على:
${Object.keys(crateReward).filter(v => v && crateReward[v] && !/الصندوق الاسطوري |حيوان|الصندوق الاسطوري المميز |الماس|زمرد/i.test(v)).map(reward => `
*${global.rpg.emoticon(reward)}${reward}:* ${crateReward[reward]}
`.trim()).join('\n')}
`.trim())
    let diamond = crateReward.diamond, mythic = crateReward.mythic, pet = crateReward.pet, legendary = crateReward.legendary, emerald = crateReward.emerald
    if (mythic || diamond) m.reply(`
مبارك حصلت على عنصر نادر, وهو${diamond ? `*${diamond}* ${rpg.emoticon('الماس')}الماس` : ''}${diamond && mythic ? 'و ' : ''}${mythic ? `*${mythic}* ${rpg.emoticon('الصندوق الاسطوري المميز')}الصندوق الاسطوري المميز` : ''}
`.trim())
    if (pet || legendary || emerald) m.reply(`
مبارك حصلا على عنصر مميز, وهو ${pet ? `*${pet}* ${rpg.emoticon('حيوان')}حيوان` : ''}${pet && legendary && emerald ? ', ' : (pet && legendary || legendary && emerald || emerald && pet) ? 'و ' : ''}${legendary ? `*${legendary}* ${rpg.emoticon(' الصندوق الاسطوري ')}الصندوق الاسطوري ` : ''}${pet && legendary && emerald ? 'و ' : ''}${emerald ? `*${emerald}* ${rpg.emoticon('زمرد')}زمرد` : ''}
`.trim())
}
